def dividir(num1, num2):
    divisao_inteira = num1 // num2
    resto = num1 % num2

    return divisao_inteira, resto

print(dividir(5, 2))


''''
a funcao acima recebe dos valores numéricos como parametros e, faz a divisao inteira dos valores, assim como o resto da mesma.
após as operacoes, ela retorna os resultados.
'''